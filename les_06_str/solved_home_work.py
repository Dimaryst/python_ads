length_special = len(special)  # получаем длину строчки со спец символами

# генерируем случайное число от 0 до длины алфавита в маленьком регистре
random_index_alphabet_lower = random.randint(0, length_alphabet - 1) # зачем делаем минус 1??? потому что индексация начинается с 0
# генерируем случайное число от 0 до длины алфавита в большом регистрe
random_index_alphabet_upper = random.randint(0, length_alphabet - 1)
# генерируем случайное число от 0 до длины цифр
random_index_numbers = random.randint(0, length_numbers - 1)
# генерируем случайное число от 0 до длины спец символов
random_index_special = random.randint(0, length_special - 1)

# собираем рандомные символы из всех строчек
random_symbol_alphabet_lower = alphabet_lower[random_index_alphabet_lower]  # берем символ из строчки с маленькими буквами
