random_index = random.randint(0, len(alphabet_upper) - 1)  # генерируем случайный индекс от 0 до длины строчки с алфавитом в большом регистре
password += alphabet_upper[random_index]  # берем случайный символ из строчки с алфавитом в большом регистре
random_index = random.randint(0, len(numbers) - 1)  # генерируем случайный индекс от 0 до длины строчки с цифрами
password += numbers[random_index]  # берем случайный символ из строчки с цифрами
random_index = random.randint(0, len(special) - 1)  # генерируем случайный индекс от 0 до длины строчки со спец символами
password += special[random_index]  # берем случайный символ из строчки со спец символами

print(password)

