import random
alphabet_lower = "abcdefghijklmnopqrstuvwxyz"
numbers = "1234567890"
special = "!@#$%^&*"
alphabet_upper = alphabet_lower.upper()

random_index = random.randint(0, len(alphabet_lower) - 1)  # генерируем случайный индекс от 0 до длины строчки с алфавитом в маленьком регистре
