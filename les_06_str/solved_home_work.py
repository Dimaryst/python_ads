# задание 4
"""
Напишите программу, которая запрашивает в терминале 
Фамилию, Имя и возраст в одну строчку, например ("  Макс Зарев 31 ")
программа должна удалять пустые символы до и после введенных данных, если пользователь ввел лишние пробелы,
и выводит в терминале сообщение, где имя должно быть написано 3 раза, фамилия 4 раза,
а возраст на 10 лет старше введенного, например (Макс Макс Макс Зарев Зарев Зарев Зарев 41)
"""
person_data = input("Введите Фамилию, Имя и возраст: ")  # запрашиваем Фамилию, Имя и возраст у пользователя
person_data = person_data.strip()  # удаляем пробелы до и после строки
name, surname, age = person_data.split()  # разбиваем строку на 3 переменные по пробелу

# обратите внимание на тип данных, он везде str
print(f"name: {name}, type: {type(name)}, surname: {surname}, type: {type(surname)}, age: {age}, type: {type(age)}")
# выводим в терминале Фамилию, Имя и возраст, возраст предварительно конвертируем в int для сложения
print(f"{(name + ' ') * 3}{(surname + ' ') * 4}{int(age) + 10}")  # к строчкам прибавляю пробелы, чтобы не было склеивания
# либо можно сделать проще
print(f"{name} {name} {name} {surname} {surname} {surname} {surname} {int(age) + 10}")




