"""
Напишите программу которая отбирает рандомные кошельки для раздачи дропа (шанс 50/50).
Программа должна запрашивать у пользователя количество кошельков участвующих в дропе и количество дропов.
если пользователь вводит не число, программа должна сообщать об ошибке и запрашивать ввести число снова.
Один кошелек может получить несколько дропов. (вот такой странный дроп)
Программа должна выводить в терминале номера кошельков, которые получили или не получили дроп, в формате:
"Кошелек номер: 1 получил дроп"
"Кошелек номер: 2 не получил дроп"
и т.д.
Если кошелек получил дроп, то сразу после сообщения о получении дропа, должен запускаться процесс клейма дропа и писать об этом, 
но перед этим нужно дождаться, когда газ будет ниже 30.
Стартовый газ 50, если газ выше 30, то программа встает на паузу в одну секунду, печатает об этом сообщение, 
потом генерирует новый газ от 15 до 50 и снова проверяет.

В конце программы должно быть сообщение: 
- строчку с перечислением номеров кошельков с дропом в формате "Кошельки с дропом: 1, 3, 5"

Программа должна раздать ровно столько дропов сколько ввел пользователь.
Программа должна быть написана с while, без использования for, списков или функций, только хардкор, страдайте :)

"""
import time
import random

gas_price = 50
while True:
    wallets_amount = input("Введите количество кошельков: ")
    if wallets_amount.isdigit():
        wallets_amount = int(wallets_amount)
        break
    else:
        print("Ошибка, введите число")

while True:
    drops_amount = input("Введите количество дропов: ")
    if drops_amount.isdigit():
        drops_amount = int(drops_amount)
        break
    else:
        print("Ошибка, введите число")

wallets_with_drop = ''
wallet_number = 0

while wallet_number < wallets_amount:

    wallet_number += 1

    if random.randint(0, 2) and drops_amount > 0:
        print(f"Кошелек номер: {wallet_number} получил дроп")
        wallets_with_drop += f"{wallet_number}, "
        while gas_price > 50:
            print(f"Цена на газ {gas_price} выше порога 20")
            time.sleep(1)
            gas_price = random.randint(15, 50)
        print(f"Дроп клеймится, газ {gas_price}")
        drops_amount -= 1
        gas_price = 50
    else:
        print(f"Кошелек номер: {wallet_number} не получил дроп")

    if drops_amount != 0 and wallet_number == wallets_amount:
        wallet_number = 0


print(f"Кошельки с дропом: {wallets_with_drop}")


